# Initialize variables
x = 4
y = 2

# Perform operations
addition = x + y
subtraction = x - y
multiplication = x * y
division = x / y
exponent = x ** y
modulus = x % y

# Print results formatted to 3 decimal points
print(f"Addition: {addition:.3f}")
print(f"Subtraction: {subtraction:.3f}")
print(f"Multiplication: {multiplication:.3f}")
print(f"Division: {division:.3f}")
print(f"Exponent: {exponent:.3f}")
print(f"Modulus: {modulus:.3f}")
